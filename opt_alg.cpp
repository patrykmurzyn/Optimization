double* expansion(matrix(*ff)(matrix, matrix, matrix), double x0, double d, double alpha, int Nmax, matrix ud1, matrix ud2)
{
	try
	{
		double* p = new double[2]{ 0,0 };

        double x1 = x0 + d;

        if(f(x1, ff) == f(x0, ff)) {
            p[0] = x0;
            p[1] = x1;
            return p;
        }

        if(f(x1, ff) > f(x0, ff)) {
            d *= -1;
            x1 = x0 +d;

            if(f(x1, ff) >= f(x0, ff)) {
                p[0] = x1;
                p[1] = x0 - d;
                return p;

            }
        }

        double *xi = new double[3] {x0, x0, x0};

        int i = 0; //licznik iteracji

        do {
            if(i > Nmax) {
                std::cout << "Error: The maximum number of iterations has been exceeded!" << std::endl;
                break;
            }
            i++;
            xi[0] = xi[1];
            xi[1] = xi[2];
            xi[2] = x0 + pow(alpha, i) * d;

        } while(f(xi[1], ff) >= f(xi[2], ff));

        if(d > 0) {
            p[0] = xi[0];
            p[1] = xi[2];
        } else {
            p[0] = xi[2];
            p[1] = xi[0];
        }
		return p;
	}
	catch (string ex_info)
	{
		throw ("double* expansion(...):\n" + ex_info);
	}
}

